class Pentomino extends Polyomino{ 
    constructor(type, shape, color, center, x, y){
        super(type, shape, color, center, x, y);
    }

    // 汎用回転テーブル: 重心がずれている形状のために全方向(±1, ±2)を試行する
    static SRS_General = {
        "0>R": [[0,0],[-1,0],[-1,1],[0,1],[1,1],[1,0],[-2,0],[0,-2]],
        "R>0": [[0,0],[1,0],[1,-1],[0,-1],[-1,-1],[-1,0],[2,0],[0,2]],
        "R>2": [[0,0],[1,0],[1,1],[0,1],[-1,1],[-1,0],[2,0],[0,2]],
        "2>R": [[0,0],[-1,0],[-1,-1],[0,-1],[1,-1],[1,0],[-2,0],[0,-2]],
        "2>L": [[0,0],[1,0],[1,1],[0,1],[-1,1],[-1,0],[2,0],[0,2]],
        "L>2": [[0,0],[-1,0],[-1,-1],[0,-1],[1,-1],[1,0],[-2,0],[0,-2]],
        "L>0": [[0,0],[-1,0],[-1,1],[0,1],[1,1],[1,0],[-2,0],[0,-2]],
        "0>L": [[0,0],[1,0],[1,-1],[0,-1],[-1,-1],[-1,0],[2,0],[0,2]]
    };

    // I型用テーブル: 長さ5のため、より遠く(±3)までキック判定を行う
    static SRS_I = {
        "0>R": [[0,0],[-2,0],[1,0],[-2,1],[1,-2],[0,1],[0,-2],[-3,0],[3,0]],
        "R>0": [[0,0],[2,0],[-1,0],[2,-1],[-1,2],[0,-1],[0,2],[3,0],[-3,0]],
        "R>2": [[0,0],[-1,0],[2,0],[-1,-2],[2,1],[0,1],[0,-2],[-3,0],[3,0]],
        "2>R": [[0,0],[1,0],[-2,0],[1,2],[-2,-1],[0,-1],[0,2],[3,0],[-3,0]],
        "2>L": [[0,0],[2,0],[-1,0],[2,-1],[-1,2],[0,-1],[0,2],[3,0],[-3,0]],
        "L>2": [[0,0],[-2,0],[1,0],[-2,1],[1,-2],[0,1],[0,-2],[-3,0],[3,0]],
        "L>0": [[0,0],[1,0],[-2,0],[1,2],[-2,-1],[0,-1],[0,2],[3,0],[-3,0]],
        "0>L": [[0,0],[-1,0],[2,0],[-1,-2],[2,1],[0,1],[0,-2],[-3,0],[3,0]]
    };

    getTable(){
        // I型かそれ以外かで適用するテーブルを変える
        return (this.type === "I") ? Pentomino.SRS_I : Pentomino.SRS_General;
    }
}